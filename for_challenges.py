# Задание 1
# Необходимо вывести имена всех учеников из списка с новой строки

names = ['Оля', 'Петя', 'Вася', 'Маша']
# Функция для вывода построчно имен учеников
def name_one(names_list: list) -> None:
    print('Задание 1:')
    # Перебор списка
    for name in names_list:
        print(f'{name}')
    print()
# Тест
name_one(names)

