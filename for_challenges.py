# Задание 3
# Необходимо вывести имена всех учеников из списка, рядом с именем вывести пол ученика

is_male = {
    'Оля': False,  # если False, то пол женский
    'Петя': True,  # если True, то пол мужской
    'Вася': True,
    'Маша': False,
}
#names = ['Оля', 'Петя', 'Вася', 'Маша']
# Функция для вывода построчно имен учеников и их пола
def name_three(names_list: list) -> None:
    print('Задание 3:')
    # Перебор списка
    for name in names_list:
        # Проверка мужской ли пол
        if is_male.get(name):
            gender = 'мужской'
        else:
            gender = 'женский'
        print(f'{name}: {gender}')
    print()
# Тест
name_three(names)

# Задание 4
# Даны группу учеников. Нужно вывести количество групп и для каждой группы – количество учеников в ней
# Пример вывода:
# Всего 2 группы.
# Группа 1: 2 ученика.
# Группа 2: 4 ученика.

groups = [
    ['Вася', 'Маша'],
    ['Вася', 'Маша', 'Саша', 'Женя'],
    ['Оля', 'Петя', 'Гриша'],
]
# Функция для вывода количества учеников в группах
def groups_one(groups_list: str) -> None:
    print('Задание 4:')
    # Счетчик групп
    i = 1
    # Перебор списка
    for groups in groups_list:
        print(f'Группа {i}: {len(groups)} ученика.')
        i = i + 1
    print()
# Тест
groups_one(groups)

